package com.pojo;

import java.sql.Date;

public class ship {
    int sid;
    int eid;
    float bship;
    float aship;
    float gship;
    float total;
    Date sdate;
    int id;

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public int getEid() {
        return eid;
    }

    public void setEid(int eid) {
        this.eid = eid;
    }

    public float getBship() {
        return bship;
    }

    public void setBship(float bship) {
        this.bship = bship;
    }

    public float getAship() {
        return aship;
    }

    public void setAship(float aship) {
        this.aship = aship;
    }

    public float getGship() {
        return gship;
    }

    public void setGship(float gship) {
        this.gship = gship;
    }

    public float getTotal() {
        return total;
    }

    public void setTotal(float total) {
        this.total = total;
    }

    public Date getSdate() {
        return sdate;
    }

    public void setSdate(Date sdate) {
        this.sdate = sdate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
